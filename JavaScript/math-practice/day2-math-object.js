// Round a number
console.log("Round:", Math.round(4.7)); // 5
console.log("Ceil:", Math.ceil(4.2));   // 5
console.log("Floor:", Math.floor(4.8)); // 4

// Absolute value
console.log("Absolute:", Math.abs(-10)); // 10

// Power and square root
console.log("2^3:", Math.pow(2, 3));       // 8
console.log("Square root of 64:", Math.sqrt(64)); // 8

// Min and Max
console.log("Min:", Math.min(1, 4, -3)); // -3
console.log("Max:", Math.max(1, 4, -3)); // 4

// Random number between 0 and 1
console.log("Random (0-1):", Math.random());

// Random between 1 and 100
let rand100 = Math.floor(Math.random() * 100) + 1;
console.log("Random (1-100):", rand100);
